package com.zhuwentao.bean;

public class ExamOutlineBean {

	private int id;
	private String outlinetitle;
	private String outlinecontent;
	private String outlinekind;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOutlinetitle() {
		return outlinetitle;
	}
	public void setOutlinetitle(String outlinetitle) {
		this.outlinetitle = outlinetitle;
	}
	public String getOutlinecontent() {
		return outlinecontent;
	}
	public void setOutlinecontent(String outlinecontent) {
		this.outlinecontent = outlinecontent;
	}
	public String getOutlinekind() {
		return outlinekind;
	}
	public void setOutlinekind(String outlinekind) {
		this.outlinekind = outlinekind;
	}
}
